package org.bartram.vidtag.model;

/**
 * Verbosity level for tag generation.
 */
public enum Verbosity {
    /**
     * Generate minimal tags.
     */
    MINIMAL,

    /**
     * Generate standard set of tags.
     */
    STANDARD,

    /**
     * Generate detailed tags.
     */
    DETAILED,

    /**
     * Generate verbose, comprehensive tags.
     */
    VERBOSE
}
